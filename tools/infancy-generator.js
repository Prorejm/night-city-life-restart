function makeInfancyEvent(id, eventText, range, withItemAward = false) {
  const ev = {
    id,
    event: eventText,
    type: 'infancy',
    include: `TURN>=${range.turnMin}&TURN<=${range.turnMax}`,
    repeatable: true,
    cooldown: randInt(3, 8),
  };

  // 生成 effect（幼儿期变化小）
  const effect = {};
  if (maybe(0.6)) {
    const prop = pick(['STYLE', 'TECH']);
    effect[prop] = maybe(0.6) ? 1 : -1;
  }
  if (maybe(0.15)) {
    effect.CHROME = 1; // 极少，义体接触
  }
  if (maybe(0.1)) {
    effect.HUMANITY = -1; // 创伤事件
  }
  if (maybe(0.25)) {
    effect.EDDIES = maybe(0.5) ? randInt(1, 2) : -randInt(1, 2);
  }
  if (Object.keys(effect).length > 0) {
    ev.effect = effect;
  } else {
    ev.effect = {};
  }

  // 约5%有itemAward
  if (withItemAward && (cyberIds.length > 0 || drugIds.length > 0)) {
    const pool = [...cyberIds, ...drugIds];
    if (maybe(0.05) && pool.length > 0) {
      ev.itemAward = pick(pool);
    }
  }

  return ev;
}

