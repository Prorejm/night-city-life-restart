const __dirname = dirname(fileURLToPath(import.meta.url));
const DATA_DIR = join(__dirname, '..', 'data');

// 读取已有物品
const items = JSON.parse(readFileSync(join(DATA_DIR, 'items.json'), 'utf-8'));
const vehicles = JSON.parse(readFileSync(join(DATA_DIR, 'vehicles.json'), 'utf-8'));

// 所有可用物品ID
const allItemIds = [...Object.keys(items)];
const wpnIds = Object.keys(items).filter(k => k.startsWith('wpn_'));
const cyberIds = Object.keys(items).filter(k => k.startsWith('cyber_') || k.startsWith('imp_'));
const drugIds = Object.keys(items).filter(k => k.startsWith('drug_'));

// 随机工具
function randInt(min, max) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}
function pick(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}
function pickN(arr, n) {
  const shuffled = [...arr].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(n, arr.length));
}
function maybe(chance) {
  return Math.random() < chance;
}

// 生成effect
function makeEffect(type, isMajor) {
  const eff = {};
  const eddies = isMajor ? randInt(2, 6) : randInt(1, 6);
  if (maybe(0.75)) {
    eff.EDDIES = maybe(0.45) ? -eddies : eddies;
  }
  if (maybe(0.3)) {
    eff.STYLE = maybe(0.7) ? randInt(1, 2) : -randInt(1, 2);
  }
  if (maybe(0.25)) {
    eff.TECH = maybe(0.7) ? randInt(1, 2) : -randInt(1, 2);
  }
  if (maybe(0.15)) {
    eff.CHROME = randInt(1, 2);
  }
  if (maybe(0.2) && type !== 'daily') {
    eff.HUMANITY = -randInt(1, 2);
  }
  if (type === 'combat' && maybe(0.5)) {
    eff.LIFE = -1;
  }
  return eff;
}

// 生成tag
function makeTags(type, extra) {
  const base = [type];
  if (extra) extra.forEach(t => base.push(t));
  return base;
}

// 决定repeatable和cooldown
function makeRepeatable(type) {
  const storyTypes = ['special', 'story'];
  if (storyTypes.includes(type)) return { repeatable: false };
  return { repeatable: true, cooldown: randInt(3, 10) };
}

// 决定是否给物品奖励（5-8%概率）
function maybeItemAward(type) {
  if (!maybe(0.07)) return {};
  const pool = type === 'medical' ? [...drugIds, ...cyberIds] :
               type === 'combat' ? wpnIds :
               [...allItemIds];
  return { itemAward: pick(pool) };
}

