// 主生成函数
// ============================================================

function generateEvents(templatePool, varFunc, startId, count, eventType, isMajor) {
  const events = [];
  const usedEvents = new Set();

  for (let i = 0; i < count; i++) {
    const id = startId + i;
    let attempts = 0;
    let eventText;

    // 尝试生成不重复的事件文本
    do {
      const template = pick(templatePool);
      const vars = varFunc();
      eventText = template(vars);
      attempts++;
    } while (usedEvents.has(eventText) && attempts < 50);

    usedEvents.add(eventText);

    const event = {
      id,
      event: eventText,
      type: eventType,
      effect: makeEffect(eventType, isMajor),
      ...makeRepeatable(eventType),
      tags: makeTags(eventType, eventType === 'daily' ? ['daily'] : []),
    };

    // 少量事件有物品奖励
    const award = maybeItemAward(eventType);
    Object.assign(event, award);

    events.push(event);
  }

  return events;
}

